"""Eval domain models: cases, graders, and results.

This is the frozen canonical eval-case format, shared cross-language with the
CLI. The Pydantic models here are the single source of truth; the committed JSON
Schema at ``apps/worker/schema/eval-cases.schema.json`` (generated by
``schema_export.py``) and the CLI's hand-mirrored Rust structs are derivatives
kept honest by a drift gate. Do not change the case, grader, or suite shape
without regenerating the schema and updating the CLI in the same reviewed change.

An eval suite is a set of cases; each case is an input prompt plus a grader that
decides pass/fail from the agent's answer. Graders are deny-by-default: a case
must name one, so a suite can never "pass" by omission. The result types roll a
run up into the ``N/M passed`` summary the PR check reports and the per-case rows
the eval matrix records per version.
"""

from __future__ import annotations

import re
from collections.abc import Sequence
from enum import StrEnum

from pydantic import (
    BaseModel,
    ConfigDict,
    Field,
    ValidationInfo,
    computed_field,
    field_validator,
)


class GraderKind(StrEnum):
    """How a case's expected value is compared against the agent's output.

    The three text matchers (``exact``/``contains``/``regex``) grade the turn's
    final answer TEXT. ``tool_called`` is the ADR-0022 Phase 1 addition: it grades
    the turn's tool-call TRAJECTORY instead, asserting a named tool was actually
    invoked during the turn. This closes the "grade the last sentence" gap -- a
    text grader can confirm a version-shaped string appears in the reply but never
    that the tool which was supposed to produce it ever ran. Reading the observed
    trajectory (not the final text) is the whole point: grepping the answer for
    the tool's name would be the mechanically-easy false green ADR-0022 warns
    against.
    """

    EXACT = "exact"
    CONTAINS = "contains"
    REGEX = "regex"
    TOOL_CALLED = "tool_called"


class ExpectedStatus(StrEnum):
    """The terminal session status an eval case asserts.

    ``done`` = the turn completed and answered; ``awaiting-approval`` = an
    approval gate correctly held (ADR-0010, epic #22). This is a deliberate
    subset of ``SessionStatus``: a classified-failure is infra breakage, never an
    expectable success, and idle-awaiting-input is out of scope. The set is
    additive and can be widened later.
    """

    DONE = "done"
    AWAITING_APPROVAL = "awaiting-approval"


class Grader(BaseModel):
    """A single deterministic grader.

    For the text matchers (``exact``/``contains``/``regex``) ``expected`` is the
    string to match against the answer text. For ``tool_called`` ``expected`` is
    instead the *tool name* that must have been invoked during the turn; the
    grader then reads the observed tool-call trajectory rather than the answer
    text. Reusing ``expected`` as the tool name keeps this an additive enum value
    with no new field -- the smallest possible touch to the frozen eval-case
    schema (ADR-0022 Phase 1).

    (An LLM-judge grader is a later addition governed by ADR-0042; keeping these
    deterministic makes eval results reproducible, which is the whole point of
    eval-as-CI.)
    """

    model_config = ConfigDict(frozen=True)

    kind: GraderKind
    expected: str
    case_sensitive: bool = False

    @field_validator("expected")
    @classmethod
    def _expected_is_well_formed(cls, value: str, info: ValidationInfo) -> str:
        """Reject an ill-formed ``expected`` eagerly, by grader kind.

        ``kind`` is declared before ``expected``, so it is already validated and
        available in ``info.data`` here. A ``regex`` grader must compile (note that
        Python ``re`` accepts a wider dialect than the CLI's Rust ``regex`` engine
        -- lookaround, backreferences -- so a pattern valid here may still be
        rejected by the local CLI; see docs/adr/0019-freeze-eval-case-format.md). A
        ``tool_called`` grader's ``expected`` is a tool name, which must be
        non-empty: an empty tool name would match no tool and can never be
        satisfied, so it is a suite-authoring mistake rather than a valid case.
        """
        kind = info.data.get("kind")
        if kind is GraderKind.REGEX:
            try:
                re.compile(value)
            except re.error as exc:
                raise ValueError(f"invalid regex pattern: {exc}") from exc
        elif kind is GraderKind.TOOL_CALLED and not value.strip():
            raise ValueError("tool_called grader requires a non-empty tool name in `expected`")
        return value

    def grade(self, output: str, trajectory: Sequence[str] = ()) -> bool:
        """True if the turn satisfies this grader.

        ``output`` is the graded answer text; ``trajectory`` is the ordered tool
        names the turn invoked (the ``tool`` field of each ``tool_note`` frame, in
        emission order). The text matchers judge ``output`` and ignore
        ``trajectory``; ``tool_called`` judges ``trajectory`` and ignores
        ``output``. Tool names are exact identifiers, so ``tool_called`` compares
        them exactly and does not fold case (the ``case_sensitive`` flag is a
        text-matcher concern and does not apply).
        """
        if self.kind is GraderKind.TOOL_CALLED:
            return self.expected in trajectory

        if self.kind is GraderKind.REGEX:
            flags = 0 if self.case_sensitive else re.IGNORECASE
            return re.search(self.expected, output, flags) is not None

        actual, expected = output, self.expected
        if not self.case_sensitive:
            actual, expected = actual.lower(), expected.lower()
        if self.kind is GraderKind.EXACT:
            return actual.strip() == expected.strip()
        return expected in actual  # CONTAINS


class EvalCase(BaseModel):
    """One eval: an input prompt and the grader that judges the answer.

    ``shared_history`` is the per-case isolation opt-out (#550). Each case runs
    in a *fresh conversation* by default (``False``): the eval driver resets the
    runner's conversation before the case so it cannot answer from an earlier
    case's history instead of actually invoking its tools -- a false green for a
    side-effecting agent (the behavior under test never ran) and a silent, order-
    dependent suite. Set ``True`` to deliberately chain a case onto the prior
    case's conversation (a multi-turn scenario expressed as ordered cases); the
    driver then skips the reset so the case inherits that history. On the *first*
    case it is a no-op-with-caveat: there is no prior case to chain onto, so it
    only means "do not reset first", inheriting whatever state the runner already
    held (empty on a freshly-booted eval runner). Optional with a ``False``
    default, so it is a backward-compatible addition to the frozen eval-case
    schema (ADR-0019): an existing suite that omits it keeps decoding.

    ``expect_status`` asserts the turn's terminal session status: it defaults to
    ``done`` (keeping every pre-existing case unchanged), or ``awaiting-approval``
    to assert an approval gate blocked the action -- so a gate holding is a green
    result instead of a red one.
    """

    model_config = ConfigDict(frozen=True)

    id: str
    input: str
    grader: Grader
    shared_history: bool = False
    expect_status: ExpectedStatus = ExpectedStatus.DONE


class EvalSuite(BaseModel):
    """A named set of eval cases run together against one plugin version."""

    model_config = ConfigDict(frozen=True)

    name: str
    cases: list[EvalCase] = Field(min_length=1)


class EvalOutcome(StrEnum):
    """How a case's turn ended: graded pass/fail, or completed but never graded.

    ``PLUMBING_OK`` is the fake tier's outcome (ADR-0055): the fake model is a
    plumbing fixture, so the only thing that tier asserts is that the turn
    completed. No grader runs, so the row is neither a pass nor a fail -- keeping
    it a distinct third value is what makes it impossible to mistake for a green
    promotion gate. A fake turn that does *not* complete is still ``FAIL``: broken
    plumbing is the one thing the tier must still catch.
    """

    PASS = "pass"
    FAIL = "fail"
    PLUMBING_OK = "plumbing_ok"


class EvalCaseResult(BaseModel):
    """The outcome of running one case: its verdict, the output, and any error.

    ``passed`` is a derived view of ``outcome`` and is tri-state: ``True`` for a
    graded pass, ``False`` for a graded fail, ``None`` for a non-graded plumbing
    row. Deriving it (rather than storing it) means no caller can set the two
    inconsistently, and ``None`` keeps an unmigrated reader fail-safe: it is falsy,
    so a truthiness check under-reports rather than ever going false-green, and
    nothing ever claims a row failed that no grader judged.

    ``cost_usd`` is the dollar cost the runner attributed to this case's turn
    when the harness reported usage/pricing; it is ``None`` when cost is not
    available (e.g. the fake-model path, or a provider that reports no usage), so
    the matrix simply omits that case from a model's cost rollup rather than
    counting it as free. Not part of the frozen eval-case schema (a *result*
    shape, not a *case* shape), so it is safe to extend here.
    """

    case_id: str
    outcome: EvalOutcome
    output: str
    latency_ms: float
    error: str | None = None
    cost_usd: float | None = None

    @computed_field  # type: ignore[prop-decorator]
    @property
    def passed(self) -> bool | None:
        if self.outcome is EvalOutcome.PLUMBING_OK:
            return None
        return self.outcome is EvalOutcome.PASS


class EvalRunResult(BaseModel):
    """A whole suite run against one version: the per-case rows plus rollups.

    ``model`` is the model id the suite was run under (the eval matrix's model
    dimension: results are sliceable by model so a suite can be compared across
    models for pass-rate and cost). It is ``None`` when the run layer could not
    resolve a concrete model (the dev/test ``target_url`` shortcut evals whatever
    the runner was already booted with), in which case the recorder records no
    ``model:`` tag and the case falls into the matrix's unlabelled column.
    """

    version: str
    suite: str
    results: list[EvalCaseResult]
    model: str | None = None

    @property
    def total(self) -> int:
        return len(self.results)

    @property
    def passed_count(self) -> int:
        return sum(1 for r in self.results if r.outcome is EvalOutcome.PASS)

    @property
    def plumbing_count(self) -> int:
        return sum(1 for r in self.results if r.outcome is EvalOutcome.PLUMBING_OK)

    @property
    def graded_total(self) -> int:
        """The rows a grader actually judged: the only honest pass-rate denominator."""
        return self.total - self.plumbing_count

    def all_passed(self) -> bool:
        """Did every case pass a grader? A pure grading predicate.

        Deliberately False for an all-plumbing run: nothing was graded, so nothing
        passed. "Did anything break" is :meth:`completed_without_failure`.
        """
        return self.total > 0 and self.passed_count == self.total

    def all_plumbing(self) -> bool:
        """Every row completed but none was graded (a fake-tier run)."""
        return self.total > 0 and self.plumbing_count == self.total

    def completed_without_failure(self) -> bool:
        """Did nothing break? The operational question a process exit asks.

        An all-plumbing run is a clean completion without being a pass; a run
        carrying any FAIL is red whether or not the rest was graded.
        """
        return not any(r.outcome is EvalOutcome.FAIL for r in self.results)

    def summary(self) -> str:
        """The one-line ``34/36 passed`` string the PR check surfaces.

        A non-graded run never renders as ``N/M passed``: both ``0/3`` (a red that
        did not happen) and ``3/3`` (the false green) are lies about rows no grader
        judged, so plumbing rows are named as such and stay out of the ratio.
        """
        if self.all_plumbing():
            return f"{self.plumbing_count} plumbing OK (not graded)"
        graded = f"{self.passed_count}/{self.graded_total} passed"
        if self.plumbing_count:
            return f"{graded}, {self.plumbing_count} plumbing OK"
        return graded
